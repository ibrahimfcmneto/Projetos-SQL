--------------------------------------------------------------
-- CRIACAO DAS TABELAS
--------------------------------------------------------------
CREATE TABLE STUDENTS (
    ID_STUDENT NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FULL_NAME VARCHAR2(80) NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE NOT NULL,
    BIRTH_DATE DATE
);

CREATE TABLE COURSES (
    ID_COURSE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    COURSE_NAME VARCHAR2(100) NOT NULL,
    AREA VARCHAR2(50),
    HOURS NUMBER(4) CHECK (HOURS > 0)
);

CREATE TABLE ENROLLMENTS (
    ID_ENROLLMENT NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_STUDENT NUMBER NOT NULL,
    ID_COURSE NUMBER NOT NULL,
    ENROLL_DATE DATE DEFAULT SYSDATE,
    STATUS VARCHAR2(20) DEFAULT 'Active' CHECK (STATUS IN ('Active','Completed','Cancelled')),
    CONSTRAINT FK_ENROLLMENTS_STUDENTS FOREIGN KEY (ID_STUDENT) REFERENCES STUDENTS(ID_STUDENT),
    CONSTRAINT FK_ENROLLMENTS_COURSES FOREIGN KEY (ID_COURSE) REFERENCES COURSES(ID_COURSE)
);

